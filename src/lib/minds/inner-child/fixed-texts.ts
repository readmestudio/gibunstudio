import type { TypeCard } from "./report-types";
import { GUARDIAN_LABELS } from "./questions";
import type { GuardianType } from "./types";

/** 유료 리포트 도입부 고정 텍스트(6-1) — 관찰자 자격 부여. */
export const READ_BEFORE = `읽기 전에 한 가지. 이 리포트에는 당신의 오래된 패턴들이 기록되어 있습니다. 읽다 보면 불편한 대목도 있을 것입니다. 그때 기억할 것 — 지금 이 문장을 읽고 있는 것은 그 아이가 아니라 당신입니다. 아이의 패턴을 바깥에서 읽어낼 수 있다는 사실 자체가, 당신 안에 아이보다 큰 자리가 있다는 증거입니다. 이 리포트는 그 자리에서 읽어주세요.`;

/** 유료 섹션 4 도입 — 지킴이 정의 블록(6-3). {지킴이 라벨} 치환. */
export function guardianDefinitionBlock(guardianType: GuardianType): string {
  const label = GUARDIAN_LABELS[guardianType];
  return `지킴이란 무엇인가

마음은 하나의 목소리가 아닙니다. 심리학에서는 마음을 여러 부분(part)들이 함께 작동하는 시스템으로 봅니다. 이 리포트에서 만난 '내면 아이'가 과거의 상처를 짊어진 부분이라면, 지킴이는 그 아이의 고통이 당신의 일상을 침범하지 않도록 막아서는 부분입니다.

지킴이는 외부에서 온 존재가 아닙니다. 당신 자신의 일부이며, 대개 그 아이가 상처받던 시기에 함께 만들어졌습니다. 어린 당신이 감당할 수 없는 고통 앞에서, 마음은 스스로를 지킬 방법을 찾아야 했고 — 그렇게 채택된 생존 전략이 지금까지 작동하고 있는 것입니다.

중요한 것은 이것입니다. 지킴이는 오작동이 아닙니다. 그 시절에는 이 방식이 실제로 당신을 지켰습니다. 다만 상황이 바뀌었는데 전략이 업데이트되지 않았을 뿐입니다. 성인이 된 당신에게 지킴이의 보호는 종종 과잉 방어가 되고, 지키려던 것보다 더 많은 것을 대가로 가져갑니다.

지킴이의 작동 방식은 크게 세 가지로 나뉩니다.

· 따르는 지킴이 — 아이의 믿음을 사실로 받아들이고, 그 믿음대로 살게 합니다. 고통을 피하는 대신 익숙한 고통에 머무는 방식입니다.
· 재우는 지킴이 — 고통이 올라오는 순간 주의를 다른 곳으로 돌립니다. 몰두, 음식, 화면, 쇼핑 — 수단은 다양하지만 목적은 하나, 느끼지 않게 하는 것입니다.
· 맞서는 지킴이 — 아이의 믿음과 정반대로 행동하게 만듭니다. 부족함이 두려운 사람을 완벽주의자로, 버림이 두려운 사람을 먼저 떠나는 사람으로 만드는 방식입니다.

테스트 데이터에 따르면, 당신의 지킴이는 ${label}에 가깝습니다. 이제 이 지킴이가 구체적으로 어떻게 작동하는지 보겠습니다.`;
}

/** 무료 잠금 섹션(5-3) — 카드/채점값으로 토큰 치환. */
export function lockSection(card: TypeCard): string {
  return `그런데, 분석은 여기서 한 가지 질문에 도달합니다.

이 아이는 왜 아직도 ${card.symbol_action}. 성인이 된 당신에게는 이 ${card.key_emotion}이 더 이상 필요하지 않은데도요.

전체 리포트는 이 질문에서 시작합니다.

· 방어 시스템의 정체 — 당신의 '${card.surface_reaction}' 반응은 우연이 아닙니다. 이 아이의 고통이 올라올 때마다 작동하는 시스템이 있고, 그 시스템은 당신을 지키는 동시에 무언가를 대가로 가져가고 있습니다.
· 같은 상처가 반복되는 구조 — 왜 그 행동이 결국 가장 두려워하던 결과를 불러오는지, 당신의 응답으로 재구성한 반복 구조를 확인할 수 있습니다.
· 두 번째 아이의 신호 — 데이터에는 ${card.child_name} 외에 또 하나의 신호가 잡혀 있습니다. 특정 상황에서는 이 아이가 먼저 반응합니다.
· 이 아이가 정말 원했던 것 — 그리고 그것을 지금의 당신이 줄 수 있는 방법.`;
}

/**
 * 유료 섹션 6 — 셀프 재양육 3단계(고정). 방향: 알아차리기 → 멈추기 → 한 문장 건네기.
 * (HANDOFF 10-5 미결정 항목 초안. 카드의 reparenting_line 을 3단계 끝에 붙인다.)
 */
export function reparentingSteps(card: TypeCard): { step: string; title: string; body: string }[] {
  return [
    {
      step: "1",
      title: "알아차리기",
      body: `${card.surface_reaction} 반응이 올라오는 순간을 '아, 지금 아이가 반응하는구나'라고 이름 붙여 봅니다. 알아차림은 아이와 당신 사이에 아주 작은 틈을 만듭니다.`,
    },
    {
      step: "2",
      title: "멈추기",
      body: `그 틈에서 곧바로 행동으로 옮기지 않고 한 박자 멈춥니다. 반응을 억지로 없애는 것이 아니라, 반응과 행동 사이에 잠깐의 간격을 두는 것입니다.`,
    },
    {
      step: "3",
      title: "한 문장 건네기",
      body: `그 아이에게 당신이 직접 한 문장을 건넵니다. "${card.reparenting_line}" — 당신이 아이에게 해줄 수 있는, 그때는 아무도 해주지 않았던 말입니다.`,
    },
  ];
}
