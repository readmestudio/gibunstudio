export type WritingToneIntensity = 'literary' | 'balanced' | 'practical';

/**
 * 카드 성격에 따라 톤 강도를 조절하는 지시문을 반환합니다.
 *
 * - literary: 인트로, 편지 등 감성적 카드. 비유와 짧은 파편 문장을 적극 사용.
 * - balanced: 분석 카드. 담백한 톤 유지하되 장면 묘사 1~2개 포함.
 * - practical: CTA, 도구 소개 등 실용적 카드. 간결하고 명확하게.
 */
export function getWritingToneDirective(intensity: WritingToneIntensity): string {
  switch (intensity) {
    case 'literary':
      return `■ 톤 지시: literary
- 비유와 감각적 이미지를 적극 사용하세요.
- 짧은 파편 문장(15자 이하)을 전체 문장의 30% 이상 포함하세요.
- 장면으로 시작하고 장면으로 끝내세요.
- 설명은 최소화하고 여운을 남기세요.`;

    case 'balanced':
      return `■ 톤 지시: balanced
- 분석 내용은 정확하게 전달하되, 톤은 담백하게 유지하세요.
- 각 섹션에 보편적 행동 패턴 1~2개 + 각 패턴에 "왜 이런 행동을 하는지" 인사이트 1줄을 결합하세요.
- 형용사를 절제하고, 행동 패턴과 인사이트로 대체하세요. "깊은", "따뜻한" 등은 한 카드에 최대 1회.
- 짧은 파편 문장(15자 이하)을 전체 문장의 20% 이상 포함하세요.
  → 긴 문장 뒤에 "그런 거예요.", "알고 있었어요.", "매번." 같은 극짧은 문장을 넣으세요.
- 볼드 섹션 타이틀 바로 아래에 "→ 핵심 요약 한 줄"을 넣으세요.
- 틀릴 수 있는 구체적 상황(시간대, 장소, 직업, 과거 경험)을 특정하지 마세요. 행동 패턴은 구체적으로, 상황은 추상적으로.`;

    case 'practical':
      return `■ 톤 지시: practical
- 명확하고 간결하게 쓰세요.
- 호소나 마케팅 톤 없이 사실을 담담하게 전달하세요.
- 필요한 정보만 빠짐없이. 군더더기 없이.
- 한 문장이 하나의 정보를 담도록 하세요.`;
  }
}


