export type WritingToneIntensity = 'literary' | 'balanced' | 'practical' | 'persuasive';

/**
 * 카드 성격에 따라 톤 강도를 조절하는 지시문을 반환합니다.
 *
 * - literary: 인트로, 편지 등 감성적 카드. 비유와 짧은 파편 문장을 적극 사용.
 * - balanced: 분석 카드. 담백한 톤 유지하되 장면 묘사 1~2개 포함.
 * - practical: CTA, 도구 소개 등 실용적 카드. 간결하고 명확하게.
 */
export function getWritingToneDirective(intensity: WritingToneIntensity): string {
  switch (intensity) {
    case 'literary':
      return `■ 톤 지시: literary
- 비유와 감각적 이미지를 적극 사용하세요.
- 짧은 파편 문장(15자 이하)을 전체 문장의 30% 이상 포함하세요.
- 장면으로 시작하고 장면으로 끝내세요.
- 설명은 최소화하고 여운을 남기세요.`;

    case 'balanced':
      return `■ 톤 지시: balanced
- 분석 내용은 정확하게 전달하되, 톤은 담백하게 유지하세요.
- 각 섹션에 보편적 행동 패턴 1~2개 + 각 패턴에 "왜 이런 행동을 하는지" 인사이트 1줄을 결합하세요.
- 형용사를 절제하고, 행동 패턴과 인사이트로 대체하세요. "깊은", "따뜻한" 등은 한 카드에 최대 1회.
- 짧은 파편 문장(15자 이하)을 전체 문장의 15% 이상 포함하세요. 파편도 완성된 문장이어야 합니다.
  → 긴 문장 뒤에 "그런 거예요.", "알고 있었어요.", "매번 그래요." 같은 극짧은 완성 문장을 넣으세요.
  → ❌ 명사구 파편("존재가 가벼워지는 느낌.") 금지. 파편이 보충 설명이면 앞 문장에 통합하세요.
- 볼드 섹션 타이틀 바로 아래에 "→ 핵심 요약 한 줄"을 넣으세요.
  → 섹션 요약(→)은 "~하는 사람" 대신 비유나 상징으로 표현하세요.
  → 예: "→ 모두의 우산이 되어주지만 정작 자기는 비를 맞는 사람"
- 하나의 인사이트를 2~3문장에 걸쳐 소설적으로 풀어쓰세요. 한 문장으로 끝내지 마세요.
- 틀릴 수 있는 구체적 상황(시간대, 장소, 직업, 과거 경험)을 특정하지 마세요. 행동 패턴은 구체적으로, 상황은 추상적으로.`;

    case 'persuasive':
      return `■ 톤 지시: persuasive (카드 8 전용)
- 조곤조곤 설명하듯 논리적으로 설득하세요. "~이런 사람이에요. ~할 때 ~하는 타입이에요."
- fragment(파편) 문장을 최소화하세요. 대신 2~3문장이 자연스럽게 이어지는 설명체를 쓰세요.
- 사용자의 TCI 분석 결과(topBottom.top1, top2, bottom)를 근거로 "왜 이 사람인지" 논리적으로 설명하세요.
- "그래서 좋은 거예요" 같은 짧은 결론 금지 → "왜 좋은지"를 2~3문장으로 풀어서 설명하세요.
- 각 섹션은 2~3문단의 설명체로 작성하세요. 단문 나열 금지.
- 볼드 섹션 타이틀 바로 아래에 "→ 핵심 요약 한 줄"을 넣으세요.
- 형용사 절제, 행동 패턴 + 인사이트로 대체. "깊은", "따뜻한" 등은 최대 1회.
- 틀릴 수 있는 구체적 상황(시간대, 장소, 직업, 과거 경험)을 특정하지 마세요.`;

    case 'practical':
      return `■ 톤 지시: practical
- 명확하고 간결하게 쓰세요.
- 호소나 마케팅 톤 없이 사실을 담담하게 전달하세요.
- 필요한 정보만 빠짐없이. 군더더기 없이.
- 한 문장이 하나의 정보를 담도록 하세요.`;
  }
}


// ─────────────────────────────────────────────────────────────
// 문체 위반 검출 함수 (검증용)
// ─────────────────────────────────────────────────────────────

/**
 * 생성된 텍스트에서 문체 위반을 검출합니다.
 * LLM 생성 결과를 로깅/디버깅할 때 활용할 수 있습니다.
 *
 * @returns 위반 항목 배열 (빈 배열이면 통과)
 */
export function checkStyleViolations(text: string): string[] {
  const violations: string[] = [];

  // 1. 절대 금지 형용사 (0회)
  const bannedWords = ['특별한', '소중한', '아름다운', '풍부한', '풍요로운', '위대한'];
  for (const word of bannedWords) {
    const regex = new RegExp(word, 'g');
    const matches = text.match(regex);
    if (matches && matches.length >= 1) {
      violations.push(`절대 금지 형용사: "${word}" ${matches.length}회 사용 (0회 허용)`);
    }
  }

  // 2. 최대 1회 형용사 (2회 이상이면 위반)
  const limitedWords = ['깊은', '따뜻한', '진정한', '건강한', '강력한', '순수한', '거대한', '단단한'];
  for (const word of limitedWords) {
    const regex = new RegExp(word, 'g');
    const matches = text.match(regex);
    if (matches && matches.length >= 2) {
      violations.push(`과잉 형용사: "${word}" ${matches.length}회 반복 (최대 1회 허용)`);
    }
  }

  // 3. "깊은+명사" 조합 경고 (1회라도 경고)
  const deepNounPatterns = /깊은\s*(공감|내면|이해|연결|감정|사랑|관계|세계|의미)/g;
  const deepNounMatches = text.match(deepNounPatterns);
  if (deepNounMatches && deepNounMatches.length >= 1) {
    violations.push(`"깊은+명사" 조합 ${deepNounMatches.length}회 검출 → 구체적 장면으로 대체 필요`);
  }

  // 4. 호소적 표현 검출
  const appealPatterns = [
    /꼭\s*(읽어|확인|해보|시작)/g,
    /놀라운\s*결과/g,
    /지금\s*바로/g,
    /대박/g,
    /놀랍게도/g,
  ];
  for (const pattern of appealPatterns) {
    if (pattern.test(text)) {
      violations.push(`호소적 표현 검출: ${pattern.source}`);
    }
  }

  // 5. "것 같아요" 과다 사용
  const seemsMatches = text.match(/것\s*같아요/g);
  if (seemsMatches && seemsMatches.length >= 4) {
    violations.push(`"것 같아요" ${seemsMatches.length}회 사용 (3회 이하 권장)`);
  }

  // 6. 평균 문장 길이 체크
  const sentences = text.split(/[.!?。]\s*/).filter(s => s.trim().length > 0);
  if (sentences.length > 0) {
    const avgLength = sentences.reduce((sum, s) => sum + s.length, 0) / sentences.length;
    if (avgLength > 60) {
      violations.push(`평균 문장 길이 ${Math.round(avgLength)}자 (50자 내외 권장)`);
    }
  }

  // 7. 짧은 문장 비율 체크 (강화: <15% 시 위반)
  if (sentences.length >= 5) {
    const shortSentences = sentences.filter(s => s.trim().length <= 15);
    const shortRatio = shortSentences.length / sentences.length;
    if (shortRatio < 0.15) {
      violations.push(`짧은 문장(15자 이하) 비율 ${Math.round(shortRatio * 100)}% (15%+ 필수, 20%+ 권장)`);
    }
  }

  // 8. 3문장 연속 30자 이상 리듬 위반 검출
  if (sentences.length >= 3) {
    for (let i = 0; i <= sentences.length - 3; i++) {
      const three = sentences.slice(i, i + 3);
      if (three.every(s => s.trim().length > 30)) {
        violations.push(`리듬 위반: ${i + 1}~${i + 3}번째 문장이 모두 30자 초과 — 파편 문장 삽입 필요`);
        break; // 첫 번째 위반만 보고
      }
    }
  }

  // 9. 어미 검출 ("~합니다/입니다" 30%+ 시 위반)
  if (sentences.length >= 5) {
    const formalEndings = sentences.filter(s => /(?:합니다|입니다|됩니다|있습니다|없습니다|했습니다|됐습니다)\s*$/.test(s.trim()));
    const formalRatio = formalEndings.length / sentences.length;
    if (formalRatio >= 0.3) {
      violations.push(`격식체 어미 비율 ${Math.round(formalRatio * 100)}% (30% 미만 필수) — "~예요/~거예요" 사용`);
    }
  }

  // 10. 설명적 연결어 검출
  const connectorPatterns = /(?:따라서|그러므로|결론적으로|요약하자면)/g;
  const connectorMatches = text.match(connectorPatterns);
  if (connectorMatches && connectorMatches.length >= 2) {
    violations.push(`설명적 연결어 ${connectorMatches.length}회 사용`);
  }

  // 11. 틀릴 수 있는 구체적 상황 가정 검출
  const specificSituationPatterns = [
    /퇴근\s*(후|길|하고)/g,
    /출근\s*(길|하면서|전에)/g,
    /새벽\s*\d시/g,
    /밤\s*\d+시/g,
    /현관문을\s*(여|닫)/g,
    /카페에서\s*(혼자|앉아)/g,
    /조별\s*과제/g,
    /반에서/g,
    /어릴\s*때.*아이였/g,
    /처음\s*만난\s*날.*앉아/g,
  ];
  for (const pattern of specificSituationPatterns) {
    if (pattern.test(text)) {
      violations.push(`구체적 상황 가정 검출: ${pattern.source}`);
    }
  }

  return violations;
}
